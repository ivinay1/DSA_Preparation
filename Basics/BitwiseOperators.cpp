#include<iostream>

int main()
{
    // we have 6 types of Bitwise operators
    // AND (&)
    // OR (|)
    // XOR (^)
    // LEFT SHIFT (<<)
    // RIGHT SHIFT (>>)

    std::cout<<"5&7"<<(5 & 7)<<'\n';
    std::cout<<"5|7"<<(5|7)<<'\n';
    std::cout<<"5^7"<<(5^7)<<'\n';
    std::cout<<"5<<1"<<(5<<1)<<'\n';
    std::cout<<"5>>1"<<(5>>1)<<'\n';

    // we also have other type of operators 
    // unary
    // binary

    // arithmetic operator, assignment operator, relational operator

    
    return 0;
}